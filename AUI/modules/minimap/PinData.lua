local function AUI_GetUnitState(groupTag)
	if IsUnitDead(groupTag) then
		return AUI_MINIMAP_UNIT_DEAD, IsUnitFriend(groupTag) 
	elseif IsUnitInCombat(groupTag) then
		return AUI_MINIMAP_UNIT_IN_COMBAT, IsUnitFriend(groupTag) 
	end

	return AUI_MINIMAP_UNIT_NORMAL, IsUnitFriend(groupTag) 
end

function AUI.Minimap.Pin.GetGroupStatePinType(groupTag)
	local pinType = MAP_PIN_TYPE_GROUP
	
	if groupTag and AUI.Settings.Minimap.showGroupMemberStatus then	
		local unitState, isFriend = AUI_GetUnitState(groupTag)
		local isLeader = IsUnitGroupLeader(groupTag)
		if isLeader then
			if unitState == AUI_MINIMAP_UNIT_IN_COMBAT then	
				if isFriend then
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_LEADER_FRIEND_IN_COMBAT				
				else					
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_LEADER_IN_COMBAT
				end
			elseif unitState == AUI_MINIMAP_UNIT_DEAD then	
				if isFriend then
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_FRIEND_DEAD				
				else					
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_DEAD
				end
			elseif unitState == AUI_MINIMAP_UNIT_NORMAL then
				if isFriend then
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_LEADER_FRIEND
				else
					pinType = MAP_PIN_TYPE_GROUP_LEADER
				end					
			end
		else
			if unitState == AUI_MINIMAP_UNIT_IN_COMBAT then	
				if isFriend then
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_FRIEND_IN_COMBAT				
				else					
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_IN_COMBAT
				end
			elseif unitState == AUI_MINIMAP_UNIT_DEAD then	
				if isFriend then
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_FRIEND_DEAD				
				else					
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_DEAD
				end									
			elseif unitState == AUI_MINIMAP_UNIT_NORMAL then
				if isFriend then
					pinType = AUI_MINIMAP_PIN_TYPE_GROUP_FRIEND				
				end					
			end
		end
	else
		local isLeader = IsUnitGroupLeader(groupTag)
		if isLeader then
			pinType = MAP_PIN_TYPE_GROUP_LEADER
		end	
	end

	return pinType
end

