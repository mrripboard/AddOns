function AUI.Minimap.Pin.GetQuestPinType(pinType, questType, questRepeatType)
	if not AUI.Minimap.IsLoaded() then
		return pinType
	end	
	
	if (ZO_MapPin.QUEST_PIN_TYPES[pinType]) then
	
		if questType == QUEST_TYPE_MAIN_STORY then
			if pinType == MAP_PIN_TYPE_QUEST_CONDITION or pinType == MAP_PIN_TYPE_QUEST_REPEATABLE_CONDITION or pinType == MAP_PIN_TYPE_QUEST_ZONE_STORY_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_QUEST_CONDITION_MAINSTORY
			elseif pinType == MAP_PIN_TYPE_QUEST_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_QUEST_REPEATABLE_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_QUEST_ZONE_STORY_OPTIONAL_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_QUEST_OPTIONAL_CONDITION_MAINSTORY
			elseif pinType == MAP_PIN_TYPE_QUEST_ENDING or pinType == MAP_PIN_TYPE_QUEST_REPEATABLE_ENDING or pinType == MAP_PIN_TYPE_QUEST_ZONE_STORY_ENDING then
				pinType = AUI_MINIMAP_PIN_TYPE_QUEST_ENDING_MAINSTORY	
				
			elseif pinType == MAP_PIN_TYPE_TRACKED_QUEST_CONDITION or pinType == MAP_PIN_TYPE_TRACKED_QUEST_REPEATABLE_CONDITION or pinType == MAP_PIN_TYPE_TRACKED_QUEST_ZONE_STORY_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_TRACKED_QUEST_CONDITION_MAINSTORY	
			elseif pinType == MAP_PIN_TYPE_TRACKED_QUEST_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_TRACKED_QUEST_REPEATABLE_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_TRACKED_QUEST_ZONE_STORY_OPTIONAL_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_TRACKED_QUEST_OPTIONAL_CONDITION_MAINSTORY
			elseif pinType == MAP_PIN_TYPE_TRACKED_QUEST_ENDING or pinType == MAP_PIN_TYPE_TRACKED_QUEST_REPEATABLE_ENDING or pinType == MAP_PIN_TYPE_TRACKED_QUEST_ZONE_STORY_ENDING then
				pinType = AUI_MINIMAP_PIN_TYPE_TRACKED_QUEST_ENDING_MAINSTORY

			elseif pinType == MAP_PIN_TYPE_ASSISTED_QUEST_CONDITION or pinType == MAP_PIN_TYPE_ASSISTED_QUEST_REPEATABLE_CONDITION or pinType == MAP_PIN_TYPE_ASSISTED_QUEST_ZONE_STORY_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_ASSISTED_QUEST_CONDITION_MAINSTORY
			elseif pinType == MAP_PIN_TYPE_ASSISTED_QUEST_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_ASSISTED_QUEST_REPEATABLE_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_ASSISTED_QUEST_ZONE_STORY_OPTIONAL_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_ASSISTED_QUEST_OPTIONAL_CONDITION_MAINSTORY	
			elseif pinType == MAP_PIN_TYPE_ASSISTED_QUEST_ENDING or pinType == MAP_PIN_TYPE_ASSISTED_QUEST_REPEATABLE_ENDING or pinType == MAP_PIN_TYPE_ASSISTED_QUEST_ZONE_STORY_ENDING then
				pinType = AUI_MINIMAP_PIN_TYPE_ASSISTED_QUEST_ENDING_MAINSTORY										
			end		
		elseif questType == QUEST_TYPE_HOLIDAY_EVENT then
			if pinType == MAP_PIN_TYPE_QUEST_CONDITION or pinType == MAP_PIN_TYPE_QUEST_REPEATABLE_CONDITION or pinType == MAP_PIN_TYPE_QUEST_ZONE_STORY_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_QUEST_CONDITION_HOLIDAY	
			elseif pinType == MAP_PIN_TYPE_QUEST_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_QUEST_REPEATABLE_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_QUEST_ZONE_STORY_OPTIONAL_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_QUEST_OPTIONAL_CONDITION_HOLIDAY	
			elseif pinType == MAP_PIN_TYPE_QUEST_ENDING or pinType == MAP_PIN_TYPE_QUEST_REPEATABLE_ENDING or pinType == MAP_PIN_TYPE_QUEST_ZONE_STORY_ENDING then
				pinType = AUI_MINIMAP_PIN_TYPE_QUEST_ENDING_HOLIDAY	
				
			elseif pinType == MAP_PIN_TYPE_TRACKED_QUEST_CONDITION or pinType == MAP_PIN_TYPE_TRACKED_QUEST_REPEATABLE_CONDITION or pinType == MAP_PIN_TYPE_TRACKED_QUEST_ZONE_STORY_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_TRACKED_QUEST_CONDITION_HOLIDAY	
			elseif pinType == MAP_PIN_TYPE_TRACKED_QUEST_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_TRACKED_QUEST_REPEATABLE_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_TRACKED_QUEST_ZONE_STORY_OPTIONAL_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_TRACKED_QUEST_OPTIONAL_CONDITION_HOLIDAY	
			elseif pinType == MAP_PIN_TYPE_TRACKED_QUEST_ENDING or pinType == MAP_PIN_TYPE_TRACKED_QUEST_REPEATABLE_ENDING or pinType == MAP_PIN_TYPE_TRACKED_QUEST_ZONE_STORY_ENDING then
				pinType = AUI_MINIMAP_PIN_TYPE_TRACKED_QUEST_ENDING_HOLIDAY

			elseif pinType == MAP_PIN_TYPE_ASSISTED_QUEST_CONDITION or pinType == MAP_PIN_TYPE_ASSISTED_QUEST_REPEATABLE_CONDITION or pinType == MAP_PIN_TYPE_ASSISTED_QUEST_ZONE_STORY_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_ASSISTED_QUEST_CONDITION_HOLIDAY	
			elseif pinType == MAP_PIN_TYPE_ASSISTED_QUEST_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_ASSISTED_QUEST_REPEATABLE_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_ASSISTED_QUEST_ZONE_STORY_OPTIONAL_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_ASSISTED_QUEST_OPTIONAL_CONDITION_HOLIDAY	
			elseif pinType == MAP_PIN_TYPE_ASSISTED_QUEST_ENDING or pinType == MAP_PIN_TYPE_ASSISTED_QUEST_REPEATABLE_ENDING or pinType == MAP_PIN_TYPE_ASSISTED_QUEST_ZONE_STORY_ENDING then
				pinType = AUI_MINIMAP_PIN_TYPE_ASSISTED_QUEST_ENDING_HOLIDAY										
			end	
		elseif questRepeatType == QUEST_REPEAT_DAILY then
			if pinType == MAP_PIN_TYPE_QUEST_CONDITION or pinType == MAP_PIN_TYPE_QUEST_REPEATABLE_CONDITION or pinType == MAP_PIN_TYPE_QUEST_ZONE_STORY_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_QUEST_CONDITION_DAILY	
			elseif pinType == MAP_PIN_TYPE_QUEST_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_QUEST_REPEATABLE_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_QUEST_ZONE_STORY_OPTIONAL_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_QUEST_OPTIONAL_CONDITION_DAILY
			elseif pinType == MAP_PIN_TYPE_QUEST_ENDING or pinType == MAP_PIN_TYPE_QUEST_REPEATABLE_ENDING or pinType == MAP_PIN_TYPE_QUEST_ZONE_STORY_ENDING then
				pinType = AUI_MINIMAP_PIN_TYPE_QUEST_ENDING_DAILY
				
			elseif pinType == MAP_PIN_TYPE_TRACKED_QUEST_CONDITION or pinType == MAP_PIN_TYPE_TRACKED_QUEST_REPEATABLE_CONDITION or pinType == MAP_PIN_TYPE_TRACKED_QUEST_ZONE_STORY_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_TRACKED_QUEST_CONDITION_DAILY	
			elseif pinType == MAP_PIN_TYPE_TRACKED_QUEST_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_TRACKED_QUEST_REPEATABLE_OPTIONAL_CONDITION or pinType == MAP_PIN_TYPE_TRACKED_QUEST_ZONE_STORY_OPTIONAL_CONDITION then
				pinType = AUI_MINIMAP_PIN_TYPE_TRACKED_QUEST_OPTIONAL_CONDITION_DAILY
